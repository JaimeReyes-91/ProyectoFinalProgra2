    private void btnIniciarSesionActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnIniciarSesionActionPerformed
        try { 
        String nombreUsuarioIngresado = txtUsuario.getText().trim();
        String claveIngresada = txtClave.getText().trim();

        String qry = "SELECT * FROM public.usuarios WHERE nombre_usuario = ?";
        try (PreparedStatement ps = con.prepareStatement(qry)) {
            ps.setString(1, nombreUsuarioIngresado);
            try (ResultSet rs = ps.executeQuery()) {
                if (rs.next()) {
                    String clave = rs.getString("clave");
                    String nombreCompleto = rs.getString("nombre_completo");
                    
                    int idUsuario = rs.getInt("usuario_id");

                    if (claveIngresada.equals(clave)) {
                        SesionUsuario.iniciarSesion(idUsuario, nombreUsuarioIngresado);
                        JOptionPane.showMessageDialog(this, "Bienvenido: " + nombreCompleto);
                        mainRef.habilitarMenus();
                        mainRef.actualizarUsuarioActivo();
                        this.dispose();
                    } else {
                        JOptionPane.showMessageDialog(this, "Contraseña incorrecta.");
                    }
                } else {
                    JOptionPane.showMessageDialog(this, "Usuario no encontrado.");
                }
            }
        }
    } catch (SQLException e) {
        JOptionPane.showMessageDialog(this, "Error SQL: " + e.getMessage());
    }
    }//GEN-LAST:event_btnIniciarSesionActionPerformed

