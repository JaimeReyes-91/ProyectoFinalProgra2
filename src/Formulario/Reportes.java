  public void consultar(int id){
    try {
        // Consulta SQL a la tabla facturas
        String qry = "SELECT detalle_id, factura_id, producto_id, cantidad, precio_unitario FROM public.detalle_factura WHERE producto_id=?";
        PreparedStatement ps = con.prepareStatement(qry);
        ps.setInt(1, id);
        ResultSet rs = ps.executeQuery();
        
        String[] columnas = {"detalle_id","factura_id", "producto_id", "cantidad", "precio_unitario"};
            DefaultTableModel modelo = new DefaultTableModel(columnas, 0); 
            
        while (rs.next()) {
                Object[] fila = {
                    rs.getString("detalle_id"),
                    rs.getString("factura_id"),
                    rs.getString("producto_id"),
                    rs.getString("cantidad"),
                    rs.getString("precio_unitario"),
                };
                modelo.addRow(fila);
            }
       JTable tabla = new JTable(modelo);
            JScrollPane scrollPane = new JScrollPane(tabla);
            JFrame frame = new JFrame("Reporte de Ventas");
            frame.add(scrollPane);
            frame.setSize(800, 400);
            frame.setLocationRelativeTo(null); // centrar en pantalla
            frame.setVisible(true);

            rs.close();
            ps.close();

    } catch (SQLException e) {
        JOptionPane.showMessageDialog(null, "Error en la consulta: " + e.getMessage());
    }
}

 
