public void reporteProductosMasVendidos() {
    try {
        String qry = "SELECT p.producto_id, p.nombre_producto, SUM(d.cantidad) AS total_vendido " +
                     "FROM detalle_factura d " +
                     "JOIN productos p ON d.producto_id = p.producto_id " +
                     "GROUP BY p.producto_id, p.nombre_producto " +
                     "ORDER BY total_vendido DESC";

        PreparedStatement ps = con.prepareStatement(qry);
        ResultSet rs = ps.executeQuery();

        String[] columnas = {"ID Producto", "Nombre Producto", "Total Vendido"};
        DefaultTableModel modelo = new DefaultTableModel(columnas, 0);

        while (rs.next()) {
            Object[] fila = {
                rs.getInt("producto_id"),
                rs.getString("nombre_producto"),
                rs.getInt("total_vendido")
            };
            modelo.addRow(fila);
        }

        JTable tabla = new JTable(modelo);
        JScrollPane scrollPane = new JScrollPane(tabla);
        JFrame frame = new JFrame("Reporte: Productos m√°s vendidos (orden descendente");
        frame.add(scrollPane);
        frame.setSize(800, 400);
        frame.setLocationRelativeTo(null);
        frame.setVisible(true);

        rs.close();
        ps.close();

    } catch (SQLException e) {
        JOptionPane.showMessageDialog(null, "Error en la consulta: " + e.getMessage());
    }
}

